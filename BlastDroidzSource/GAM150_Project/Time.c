static Time gameTime = { 0, 0, 1, 0, 0 };

/*------------------------------------------------------------------------------
// Private Function Declarations:
//----------------------------------------------------------------------------*/

/*------------------------------------------------------------------------------
// Public Functions:
//----------------------------------------------------------------------------*/

void Time_Update(float dt)
{
  gameTime.dtPrev = gameTime.dtCur;
  gameTime.dtCur = dt;
  gameTime.sinceGameStart += dt;
  gameTime.sinceStateStart += dt;
}

void Time_ResetStateTime()
{
  gameTime.sinceStateStart = 0;
}

void Time_SetTimeScale(float timeScale)
{
  gameTime.timeScale = timeScale;
}

Time *Time_Get()
{
  return &gameTime;
}

